export function init(isRu) {
  let out = ''
  for (let i = 0; i < keyboard.length; i++) {
    if (i === 13 || i === 29 || i === 42) {
      out += '<div class = "key backspace" value="' + keyboard[i].name + '" data = "' + keyboard[i].code + '" >' + keyboard[i][isRu ? 'ruName' : "name"] + '</div>'
    } else if (i === 14) {
      out += '<div class = "key tab" value="' + keyboard[i].name + '" data = "' + keyboard[i].code + '" >' + keyboard[i][isRu ? 'ruName' : "name"] + '</div>'
    } else if (i === 41 || i === 54) {
      out += '<div class = "key enter" value="' + keyboard[i].name + '" data = "' + keyboard[i].code + '" >' + keyboard[i][isRu ? 'ruName' : "name"] + '</div>'
    } else if (i === 58) {
      out += '<div class = "key space" value="' + keyboard[i].name + '" data = "' + keyboard[i].code + '" >' + keyboard[i][isRu ? 'ruName' : "name"] + '</div>'
    } else {
      out += '<div class = "key" shift="' + keyboard[i].name + '" value="' + keyboard[i].name + '" data = "' + keyboard[i].code + '" >' + keyboard[i][isRu ? 'ruName' : "name"] + '</div>'
    }
  }
  document.querySelector('#keyboard').innerHTML = out;
}
